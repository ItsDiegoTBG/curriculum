var LLP= document.getElementById("LLP");
LLP.addEventListener('mouseover', function() {
    LLP.querySelectorAll('li').forEach(item => {
        item.style.backgroundColor = "#272343";
        item.style.color = "#fffffe";
    }); 
    LLP.querySelectorAll('h3').forEach(item => {
        item.style.backgroundColor = "#272343";
        item.style.color = "#fffffe";
    }); 
    LLP.querySelectorAll('ul').forEach(item => {
        item.style.backgroundColor = "#272343";
        item.style.color = "#fffffe";
    }); 
    this.style.backgroundColor =  "#272343"; // Cambia a azul al pasar el cursor
    this.style.color = "#fffffe";
});
LLP.addEventListener('mouseout', function() {
    LLP.querySelectorAll('li').forEach(item => {
        item.style.backgroundColor = 'rgb(169, 219, 218)';
        item.style.color = "#272343";
    }); 
    LLP.querySelectorAll('h3').forEach(item => {
        item.style.backgroundColor = 'rgb(169, 219, 218)';
        item.style.color = "#272343";
    }); 
    LLP.querySelectorAll('ul').forEach(item => {
        item.style.backgroundColor = 'rgb(169, 219, 218)';
        item.style.color = "#272343";
    }); 
    this.style.backgroundColor = 'rgb(169, 219, 218)'; // Restablece al color original
    this.style.color = "#272343";
});
